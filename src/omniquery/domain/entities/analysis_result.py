from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Optional


@dataclass
class AnalysisResult:
    """
    Entity that holds the full output of a single EDA run.

    Attributes:
        question:       The original natural language question.
        generated_sql:  The SELECT statement produced by the LLM and executed.
        raw_data:       Rows returned by the database as a list of dicts.
        report:         Markdown-formatted EDA report generated by the LLM.
        error:          Populated when any step fails; the other fields may be partial.
    """

    question: str
    generated_sql: Optional[str] = None
    raw_data: list[dict[str, Any]] = field(default_factory=list)
    report: Optional[str] = None
    error: Optional[str] = None

    # ------------------------------------------------------------------
    # Derived helpers
    # ------------------------------------------------------------------

    @property
    def row_count(self) -> int:
        return len(self.raw_data)

    @property
    def succeeded(self) -> bool:
        return self.error is None and self.report is not None

    def __str__(self) -> str:
        status = "OK" if self.succeeded else f"ERROR: {self.error}"
        return f"AnalysisResult(rows={self.row_count}, status={status})"
